/**
 *add -  adds two integers and returns the result.
 *
 * result : is the value of adding the 2 integers.
 *
 *a : input integer
 *
 *b :  second input integer
 *
 * Return : The value of result
 */

int add(int a, int b)
{
	int result = a + b;

	_putchar(result + '0');
	return (result);
}
